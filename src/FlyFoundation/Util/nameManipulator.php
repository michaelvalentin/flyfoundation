<?php


namespace FlyFoundation\Util;


class NameManipulator {

    /**
     * @param string $name
     * @return string
     */
    public function toCamelUpperCaseFirst($name)
    {
        return $name;
    }

    /**
     * @param string $name
     * @return string
     */
    public function toCamelLowerCaseFirst($name)
    {
        return $name;
    }

    /**
     * @param string $name
     * @return string
     */
    public function toUnderscored($name)
    {
        return $name;
    }
} 